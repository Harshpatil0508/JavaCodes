public class Code7 {
    public static void main(String[] args) {
        System.out.println("Arithmatic Operators in java:");
        int a = 10;
        int b = 20;
        System.out.println("a + b = " + (a + b)); // Addition
        System.out.println("a - b = " + (a - b)); // Subtraction
        System.out.println("a * b = " + (a * b)); // Multiplication
        System.out.println("b / a = " + (b / a)); // Division

        System.out.println("b % a = " + (b % a)); // Modulus
        System.out.println("a++ = " + (a++)); // Post-increment
        System.out.println("++a = " + (++a)); // Pre-increment
        System.out.println("a-- = " + (a--)); // Post-decrement
        System.out.println("--a = " + (--a)); // Pre-decrement

        int x = 1;
        int y = x++;
        System.out.println("x = " + x); // Outputs 
        System.out.println("y = " + y); // Outputs 
        
    }
}
